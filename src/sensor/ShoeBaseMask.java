	public static byte[][] getMask(Direction direction) {
		if (direction == Direction.LEFT) {
			return getMaskClone(false);
		} else {
			return getMaskClone(true);
		}
	}
	
	private static byte[][] getMaskClone(boolean reverse) {
		byte[][] clone = new byte[mask.length][];
		for (int i = 0; i < mask.length; i++) {
			clone[i] = new byte[mask[i].length];
			for (int j = 0; j < mask[i].length; j++) {
				if (reverse) {
					clone[i][j] = mask[i][mask[i].length - j - 1];
				} else {
					clone[i][j] = mask[i][j];
				}
			}
		}
		return clone;
	}
